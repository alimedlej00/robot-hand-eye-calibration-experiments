from __future__ import annotations
"""
Experimental patch: use image-based refined camera extrinsics for 3-camera triangulation.

What it does:
1. Backs up shared_inputs/tf_base_to_camera.json
2. Replaces cam1, cam2, cam3 T_4x4 using:
   3cam_experiment/results/.../tf_base_to_camera_image_based_refined.json
3. Keeps cam4 unchanged
4. Adds update_history entry

After running this, rerun:
    python run_3cam_triangulate_calibrate_validate_v2.py
or:
    python run_3cam_robust_calibrate_validate.py

Important:
This is experimental. Your previous stable result should remain backed up.
"""

import json
import shutil
from datetime import datetime
from pathlib import Path


ROOT = Path(r"C:\Users\kaysa\OneDrive\Desktop\robot-hand-eye-calibration-experiments\03_extrinsic_calibration")

SHARED_TF = ROOT / "shared_inputs" / "tf_base_to_camera.json"

# The file generated by the image-based experiment.
# Put this script in 3cam_experiment/scripts or anywhere; paths are absolute.
REFINED_TF_CANDIDATES = [
    ROOT / "3cam_experiment" / "results" / "04_image_based_extrinsic_dh_experiment" / "tf_base_to_camera_image_based_refined.json",
    ROOT / "3cam_experiment" / "results" / "image_based_extrinsic_dh_experiment" / "tf_base_to_camera_image_based_refined.json",
    ROOT / "3cam_experiment" / "results" / "tf_base_to_camera_image_based_refined.json",
    Path.cwd() / "tf_base_to_camera_image_based_refined.json",
]


def load_json(p: Path) -> dict:
    with open(p, "r", encoding="utf-8") as f:
        return json.load(f)


def save_json(p: Path, data: dict) -> None:
    with open(p, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2)


def find_refined_tf() -> Path:
    for p in REFINED_TF_CANDIDATES:
        if p.exists():
            return p
    raise FileNotFoundError(
        "Could not find tf_base_to_camera_image_based_refined.json.\n"
        "Copy it next to this script or update REFINED_TF_CANDIDATES in the script."
    )


def main() -> None:
    if not SHARED_TF.exists():
        raise FileNotFoundError(f"Shared transform file not found: {SHARED_TF}")

    refined_path = find_refined_tf()
    print(f"Using refined extrinsics from:\n  {refined_path}")

    shared = load_json(SHARED_TF)
    refined = load_json(refined_path)

    shared_extr = shared["T_base_to_camera_extrinsics"]
    refined_extr = refined["T_base_to_camera_extrinsics"]

    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    backup = SHARED_TF.with_name(f"tf_base_to_camera_BACKUP_before_image_based_extrinsics_{ts}.json")
    shutil.copy2(SHARED_TF, backup)
    print(f"Backup created:\n  {backup}")

    update_entry = {
        "update": "cam1_cam2_cam3 replaced with image-based refined extrinsics for experimental rerun",
        "backup_file": str(backup),
        "source_file": str(refined_path),
        "cameras_updated": ["cam1", "cam2", "cam3"],
        "timestamp": ts,
        "note": "Experimental; compare triangulation/calibration/validation before accepting.",
        "old_cameras": {},
        "new_cameras": {},
    }

    for cam in ["cam1", "cam2", "cam3"]:
        if cam not in refined_extr:
            raise KeyError(f"{cam} not found in refined extrinsics file.")
        if cam not in shared_extr:
            raise KeyError(f"{cam} not found in shared tf_base_to_camera.json.")

        update_entry["old_cameras"][cam] = shared_extr[cam]
        update_entry["new_cameras"][cam] = refined_extr[cam]

        # Replace full camera block, but preserve metadata if useful.
        shared_extr[cam] = refined_extr[cam]
        shared_extr[cam]["source"] = "image_based_extrinsic_plus_dh_experiment_3cam_EXPERIMENTAL_RERUN"

    shared.setdefault("update_history", []).append(update_entry)
    save_json(SHARED_TF, shared)

    print("\nDone. Replaced cam1, cam2, cam3 with image-based refined extrinsics.")
    print("\nNow rerun:")
    print("  python run_3cam_triangulate_calibrate_validate_v2.py")
    print("or:")
    print("  python run_3cam_robust_calibrate_validate.py")
    print("\nThen send me the new validation/calibration reports.")


if __name__ == "__main__":
    main()
